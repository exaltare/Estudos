namespace ExemploPOO.Models
{
    public class Claculadora
    {
        public int Somar(int num1 , int numm2)
        {
            return num1 + numm2;
            
        }

        public int Somar(int num1 , int numm2 , int num3)
        {
            return num1 + numm2 + num3;
            
        }
        
    }
}